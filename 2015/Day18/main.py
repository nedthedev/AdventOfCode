def parse(data):
	parsed = []
	new_row = []
	for row in data:
		new_row = []
		for char in row:
			new_row.append(char)
		parsed.append(new_row)
	return parsed

def get_powered(data, row, col):
	powered = 0
	for r in range(row-1, row+2):
		if(r >= 0 and r < len(data)):
			for c in range(col-1, col+2):
				if(c >= 0 and c < len(data[r]) and not (c == col and r == row)):
					if(data[r][c] == '#'):
						powered += 1
	return powered

def simulate(data, part2=False):
	state = []
	neighbors = on = 0
	for r in range(len(data)):
		state.append([])
		for c in range(len(data[r])):
			on = (data[r][c] == '#')
			neighbors = get_powered(data, r, c)
			if(on):
				if(neighbors not in [2,3]):
					state[r].append('.')
				else:
					state[r].append('#')
			else:
				if(neighbors == 3):
					state[r].append('#')
				else:
					state[r].append('.')
	if(part2):
		state[0][0] = '#'
		state[0][-1] = '#'
		state[-1][0] = '#'
		state[-1][-1] = '#'
	return state

def lights_on(data):
	powered = 0
	for r in data:
		for c in r:
			if(c == "#"):
				powered += 1
	return powered

def print_grid(data):
	for row in data:
		print(''.join(row))

def part_one(data, steps, part2=False):
	for i in range(steps):
		data = simulate(data, part2)
		# print_grid(data)
		# time.sleep(.05)
	return lights_on(data)

def part_two(data, steps):
	data[0][0] = '#'
	data[0][-1] = '#'
	data[-1][0] = '#'
	data[-1][-1] = '#'
	return part_one(data, steps, part2=True)

